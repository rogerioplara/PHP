/*
O PHP contém um conjunto de funções e mecanismos para lidar 
com erros. As funções relacionadas com erros permitem definir
regras para a apresentação ou ocultação de erros

Podemos usá-las para fazer registro de logs

O registro de logs nada mais é do que guardar um histórico
de eventos que vão ocorrendo em nossa aplicação. 
Se esse registro guardar erros que vão sendo detectados
ao longo da execução, isso permite que possamos resolver
esses erro, sabendo onde e quando ocorrem

Existem funções de log que permitem o envio de mensagens para outras máquinas, emails, ou registros de logs do sistema.

Existem funções que permitem definir o nível e tipo de erros
que serão apresentados.

Este tipo de funções são particularmente importantes na fase
de desenvolvimento de uma aplicação, uma vez que nos dão
informações detalhadas sobre os erros do nosso código.
*/